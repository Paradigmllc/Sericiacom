const LOCALES = ["en", "ja", "de", "fr", "es", "it", "ko", "zh-TW", "ru", "ar"];

let added = 0;
let skipped = 0;

for (const locale of LOCALES) {
  const filePath = path.join(MESSAGES_DIR, `${locale}.json`);
  const raw = fs.readFileSync(filePath, "utf8");
  const json = JSON.parse(raw);

  json.pages ??= {};
  for (const [pageKey, pageStrings] of Object.entries(PAGES[locale])) {
    json.pages[pageKey] ??= {};
    for (const [k, v] of Object.entries(pageStrings)) {
      if (json.pages[pageKey][k] !== undefined) {
        skipped++;
        continue;
      }
      json.pages[pageKey][k] = v;
      added++;
    }
  }

  fs.writeFileSync(filePath, JSON.stringify(json, null, 2) + "\n", "utf8");
  console.log(`[i18n-pages] ${locale}.json updated`);
}

console.log(`[i18n-pages] done — added ${added} keys, skipped ${skipped} pre-existing`);
